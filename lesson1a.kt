fun main() {
    // A variable is a container that holds data or values
    // In kotlin we have two main types of initializers i/e var and val
    // val => immutable(unchangeable) but var => mutable(changeable)

    val name = "Paul Mungah" 
    // string=> a series of  character inside of quotes
    val number1 = 5
    // integer -> whole number
    val number2 = 40.6
    // Double -> Number with decimal
    var letter = "K" 
    // Char -> it is a single letter of the alphabet
    var isRegistered = true 
    // boolean -> it evaluate to either true or false

    println(name)
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)
    
}